    public void cargarTablaServicios() {
    DetalleServicioDAO dao = new DetalleServicioDAO();
    DefaultTableModel modelo = (DefaultTableModel) tblServicios.getModel();
    modelo.setRowCount(0);

    for (Object[] fila : dao.listarServicios()) {
        modelo.addRow(fila);
    }
}
    
    
    
